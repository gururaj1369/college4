          <h5>The Program Educational Objectives (PEO's)
</h5> <br>

b>PEO 1</b>
 <p>To prepare students for a successful career in Computer Science Engineering and related industry that meets the Indian and multinational companies and to excel in higher education and research programs.
</p>
<b>PEO 2</b>
<p>To  provide  students  a  broad-based  curriculum  in  Computer Science Engineering  Stressing on fundamentals  in basic and  core  subjects such  as Mathematics, Sciences, and Engineering fundamentals required to solve engineering real time problems along with appreciation of the arts, humanities, and social sciences.
</p>
<b>PEO 3</b>
<p>To provide students with the broad- based education necessary to understand the impact of Computer Science Engineering applications and solutions in a global and societal context.
</p>
<b>PEO 4</b>
<p>To provide students with good scientific and engineering environment so as to enable them to comprehend, analyze, design and create novel products, innovations and solutions for the live problems in industry and to succeed competitive examinations of global competence.
</p>
<b>PEO 5</b>
<p>To provide students an understanding of professional and ethical responsibility, effective communication skills, team- spirit, multidisciplinary approach, and the lifelong learning needed for a successful professional career.
</p>



