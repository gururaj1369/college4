        <h2>Vision and Mission of the mechanical department</h2>
          <hr>
        <b>Vision</b>
<p>To achieve high quality Mechanical Engineers competing the global standards in education, research and services with effective communications skills and technical knowledge with the attitudes of entrepreneurial, ethical and social concern.
</p>
          <b>Mission</b>
          <p>To impart quality technical education with well-established mechanical engineering laboratories and instructions to prepare the young mechanical engineers with the knowledge of basic sciences, core values, critical thinking, information competency, effective communication skills and to compete globally in education, research and services with the attitudes of entrepreneurial, ethical and social concern.</p>
          

          

          <br>
