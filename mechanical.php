          <h5>The Program Educational Objectives (PEO's)
</h5> 


<br>


<b>PEO 1</b>
 <p> To prepare students for a successful career in Mechanical Engineering and related industry that meets the needs of India and multinational companies and to excel in post-graduate and research programs.

</p>
<b>PEO 2</b>
<p>To provide students abroad-based curriculum in Mechanical Engineering Stressing on solid foundation in core subjects such as Mathematics, Sciences, and Engineering fundamentals required to solve engineering problems along with appreciation of the arts, humanities, and social sciences.

</p>
<b>PEO 3</b>
<p>To provide students with the Abroad- based education necessary to understand the impact of Mechanical Engineering applications and solutions in a global and social context.

</p>
<b>PEO 4</b>
<p>To provide students with good scientific and engineering temperament so as to enable them to comprehend, analyze, design and create novel products innovations and solutions for the live problems in industry and to succeed competitive examinations of global competence.
</p>
<b>PEO 5</b>
<p>To provide students an understanding of professional and ethical responsibility, effective communication skills, team- spirit, multidisciplinary approach, and the lifelong learning needed for a successful professional career.

</p>

